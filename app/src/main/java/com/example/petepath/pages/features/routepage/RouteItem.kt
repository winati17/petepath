@Composable
fun RouteItem(route: String) {
    Row(
        verticalAlignment = Alignment.CenterVertically,
        modifier = Modifier
            .fillMaxWidth()
            .padding(vertical = 8.dp)
    ) {
        Box(
            modifier = Modifier
                .size(12.dp)
                .background(color = Color(0xFF007BFF), shape = CircleShape)
        )

        Spacer(modifier = Modifier.width(16.dp))

        Text(
            text = route,
            fontSize = 16.sp,
            color = Color.Black
        )
    }
}